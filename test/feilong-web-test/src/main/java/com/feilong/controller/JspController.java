package com.feilong.controller;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestMapping;

/**
 * @author <a href="mailto:venusdrogon@163.com">金鑫</a>
 * @version 1.0 2012-3-18 下午11:32:05
 */
@Controller
@SuppressWarnings("all")public class JspController{

	@SuppressWarnings("unused")
	private static final Logger	log	= LoggerFactory.getLogger(JspController.class);

	@RequestMapping(value = { "/jsp/{category}/{page}" })
	public String category(@PathVariable("category") String category,@PathVariable("page") String page){
		return "feilong." + category + "." + page;
	}

}
